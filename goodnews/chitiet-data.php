                        <p>WordPress Themes can be more faster than you think let’s see how !!?</p>

                        <p>A few months ago, I ran an experiment to see how much faster I could make one of my websites
                            in less than two hours of work. After installing a handful of WordPress plugins and fixing a
                            few simple errors, I had improved the website’s loading speed from 1.61 seconds to 583
                            milliseconds. That’s a 70.39% improvement, without having made any visual changes to the
                            website.</p>

                        <p>According to a <a
                                href="http://www.akamai.com/html/about/press/releases/2009/press_091409.html">2009
                                Akamai study</a>, 47% of visitors expect a page to load in under 2 seconds, and 57% of
                            visitors will abandon a page that takes more than 3 seconds to load. Since this study, no
                            shortage of case studies have confirmed that loading time affects sales.</p>

                        <p>In 2006, Amazon reported that a 100-millisecond increase in page speed translated to a 1%
                            increase in its revenue. Just a few years later, Google <a
                                href="http://googlewebmastercentral.blogspot.com.au/2010/04/using-site-speed-in-web-search-ranking.html">announced
                                in a blog post</a> that its algorithm takes page speed into account when ranking
                            websites.</p>
