                                    alt="Runtime by plugin" /&gt;</noscript>
                            </a><br>
                            <figcaption>(<a
                                    href="http://media.mediatemple.netdna-cdn.com/wp-content/uploads/2014/06/runtime-by-plugin-large-opt.jpg">Large
                                    preview</a>)
                            </figcaption>
                        </figure>
                        <p>A common culprit is social-sharing plugins, most of which bloat page-loading times and can
                            easily be replaced by embedding social buttons into the theme’s source code.</p>

                        <p>Once you’re aware of which plugins are slowing down your website, you can make an informed
                            decision about whether to keep them, replace them or remove them entirely.</p>
                        <h4>2. COMPRESS YOUR WEBSITE</h4>

                        <p>When you compress a file on your computer as a ZIP file, the total size of the file is
                            reduced, making it both easier and faster to send to someone. Gzip works in exactly the same
                            way but with your Web page files.</p>

                        <p>Once installed, Gzip automatically compresses your website’s files as ZIP files, saving
                            bandwidth and speeding up page-loading times. When a user visits your website, their browser
                            will automatically unzip the files and show their contents. This method of transmitting
                            content from the server to the browser is far more efficient and saves a lot of time.</p>
                        <figure><a
                                href="http://media.mediatemple.netdna-cdn.com/wp-content/uploads/2014/06/gzip-large-opt.jpg"><img
                                    src="http://media.mediatemple.netdna-cdn.com/wp-content/uploads/2014/06/gzip-preview-opt.jpg"
