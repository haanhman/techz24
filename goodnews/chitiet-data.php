                        <p>Below are twelve quick fixes that will dramatically improve your website’s loading time,
                            including:</p>
                        <ul>
                            <li>identifying which plugins are slowing down your website;</li>
                            <li>automatically compressing Web pages, images, JavaScript and CSS files;</li>
                            <li>keeping your website’s database clean;</li>
                            <li>setting up browser caching the right way.</li>
                        </ul>
                        <h3>Lay The Foundation</h3>

                        <p>When your house is sinking into the ground, you don’t polish the windows — you fix the
                            foundations. The same goes for your website. If it’s hosted on a sluggish server or has a
                            bloated theme, quick fixes won’t help. You’ll need to fix the foundation.</p>

                        <p>So, let’s start with what makes for a good foundation and how to set ourselves up for a
                            website that runs at lightening speed.</p>
                        <h4>CHOOSE A GOOD HOST</h4>

                        <p>Your Web hosting company and hosting package have a huge impact on the speed of your website,
                            among many other important performance-related things. I used to be sucked in by the allure
                            of free or cheap hosting, but with the wisdom of hindsight, I’ve learned that hosting isn’t
                            an area to skimp on.</p>

                        <p>To put this into perspective, two of my clients have similar websites but very different
                            hosting providers. One uses WPEngine (an excellent hosting company), and the other hosts
                            their website on a cheap shared server.</p>

                        <p>The DNS response time (i.e. the time it takes for the browser to connect to the hosting
                            server) of the client using WPEngine is 7 milliseconds. The client using the cheap shared
                            hosting has a DNS response time of 250 milliseconds.</p>

                        <p>If you want your website to run quickly, <a
                                href="http://www.ventureharbour.com/web-hosting-guide/">start with a good hosting
                                company</a> and package.</p>
                        <h4>CHOOSE A GOOD THEME</h4>

                        <p>Unfortunately, not all WordPress themes are created equal. While some are extremely fast and
                            well coded, others are bloated with hundreds of bells and whistles under the pretence of
                            being “versatile and customizable.”</p>

                        <p>A few years ago, Julian Fernandes of Synthesis ran an interesting case study in which he
                            updated his theme from WordPress’ default to the Genesis framework, monitoring page speed.
                            He noticed that just by changing the theme to Genesis, his loading time improved from 630 to
                            172 milliseconds.</p>

                        <p>When you choose a theme, check the page speed of the theme’s demo, using a tool such as
                            Pingdom, to see how quickly it runs with nothing added to it. This should give you an idea
                            of how well coded it is.</p>
                        <h4>USE A CONTENT DELIVERY NETWORK</h4>

                        <p>I recently started using a content delivery network (CDN) for one of my websites and noticed
                            a 55% reduction in bandwidth usage and a huge improvement in page-loading speed.</p>

                        <p>A CDN hosts your files across a huge network of servers around the world. If a user from
                            Argentina visits your website, then they would download files from the server closest to
                            them geographically. Because your bandwidth is spread across so many different servers, the
                            load on any single server is reduced.</p>

                        <p>Setting up a CDN can take a few hours, but it’s usually one of the quickest ways to
                            dramatically improve page-loading speed.</p>

                        <h3>12 Quick Fixes To Speed Up WordPress</h3>

                        <p>Now that our foundation is solid, we can begin fine-tuning our website.</p>

                        <p>A good way to start speeding up a website is to look at what can be removed. More often than
                            not, a website is slow not because of what it lacks but because of what it already has.</p>
                        <h4>1. IDENTIFY PLUGINS THAT ARE SLOWING YOU DOWN</h4>

                        <p><a href="https://wordpress.org/plugins/p3-profiler/">P3</a> is one of my favourite diagnostic
                            plugins because it shows you the impact of your other plugins on page-loading time. This
                            makes it easy to spot any plugins that are slowing down your website.</p>
                        <figure><a
                                href="http://media.mediatemple.netdna-cdn.com/wp-content/uploads/2014/06/runtime-by-plugin-large-opt.jpg"><img
                                    src="http://media.mediatemple.netdna-cdn.com/wp-content/uploads/2014/06/runtime-by-plugin-preview-opt.jpg"
