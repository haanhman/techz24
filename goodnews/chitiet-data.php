                        <p>There is virtually no downside to installing Gzip, and the increase in speed can be quite
                            dramatic. As we can see in the screenshot above, <a
                                href="http://www.musiclawcontracts.com/">MusicLawContracts.com</a> goes from 68 KB to
                            only 13 KB with Gzip installed.</p>

